//Crie um algoritmo chamado "Media" com as variaveis e calcule a media de seus
//valores
//n1 = 5, n2 = 8, n3 = 10, n4 = 2

#include <iostream>
using namespace std;
int main(){
    double n1, n2, n3, n4, m;
    n1 = 5;
    n2 = 8;
    n3 = 10;
    n4 = 2;
    m = (n1 + n2 + n3 + n4) / 4;
    cout << m << endl;
    
    return 0;
}