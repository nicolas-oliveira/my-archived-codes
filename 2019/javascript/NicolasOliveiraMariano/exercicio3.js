var numero =  Number.parseInt(window.prompt('Digite um número: '))
    
switch (numero) {
    case 1:
        window.alert(`Janeiro`)
        break;
    case 2:
        window.alert(`Fevereiro`)
        break;
    case 3:
        window.alert(`Março`)
        break;
    case 4:
        window.alert(`Abril`)
        break;
    case 5:
        window.alert(`Maio`)
        break;
    case 6:
        window.alert(`Junho`)
        break;
    case 7:
        window.alert(`Julho`)
        break;
    case 8:
        window.alert(`Agosto`)
        break;
    case 9:
        window.alert(`Setembro`)
        break;
    case 10:
        window.alert(`Outubro`)
        break;
    case 11:
        window.alert(`Novembro`)
        break;
    case 12:
        window.alert(`Dezembro`)
        break;
    
    default:
        window.alert(`${numero} não é uma escolha válida`)
        break;
}